package com.example.geekbrainshomeworkfromlesson6;

public class CardData {
    private String note;
    private String detail;

    public CardData(String note, String detail) {
        this.note = note;
        this.detail = detail;
    }

    public String getNote() {
        return note;
    }

    public String getDetail() {
        return detail;
    }
}
